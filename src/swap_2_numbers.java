public class swap_2_numbers {
    public static void main(String[] args) {
        int a =2;
        int b=4;
        int temp;

        System.out.println("A is "+a);
        System.out.println("B is "+b);
        System.out.println("\n");
        temp=a;
        a=b;
        b=temp;
        System.out.println("A is "+a);
        System.out.println("B is "+b);


    }
}
